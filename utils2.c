#include "cub3D.h"

void map_max_sz(char **map,int *size)
{
    int y;
    int tp;

    y = 0;
    tp = 0;
    size[1] = 0;
    while (map[y])
    {
        tp = str_len(map[y], 0);
        if(tp > size[1])
            size[1] = tp;
        y++;
    }
    size[0] = y;
}
